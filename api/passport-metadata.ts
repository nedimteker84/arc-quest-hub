type PassportMetadataRequest = {
  wallet: string
  level: string
  xp: number
  builderScore: number
  reputation: number
  currentStreak: number
  bestStreak: number
  totalCheckIns: number
  badges: string[]
  externalUrl: string
}

type ServerRequest = {
  method?: string
  body?: Partial<PassportMetadataRequest>
}

type ServerResponse = {
  status: (code: number) => {
    json: (body: unknown) => void
  }
}

function getServerEnv(name: string) {
  const env = (globalThis as unknown as {
    process?: {
      env?: Record<string, string | undefined>
    }
  }).process?.env

  return env?.[name]
}

function isValidMetadataPayload(body: Partial<PassportMetadataRequest>) {
  return Boolean(
    body.wallet &&
      body.level &&
      typeof body.xp === "number" &&
      typeof body.builderScore === "number" &&
      typeof body.reputation === "number" &&
      typeof body.currentStreak === "number" &&
      typeof body.bestStreak === "number" &&
      typeof body.totalCheckIns === "number" &&
      Array.isArray(body.badges) &&
      body.externalUrl,
  )
}

function escapeSvg(value: string) {
  return value
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;")
}

