export default async function handler(req: ServerRequest, res: ServerResponse) {
  if (req.method !== "POST") {
    return res.status(405).json({ error: "Method not allowed" })
  }

  const pinataJwt = getServerEnv("PINATA_JWT")

  if (!pinataJwt) {
    return res.status(500).json({ error: "PINATA_JWT is not configured." })
  }

  const body = req.body ?? {}

  if (!isValidMetadataPayload(body)) {
    return res.status(400).json({ error: "Invalid passport metadata payload." })
  }

  const typedBody = body as PassportMetadataRequest
  const svg = createPassportSvg(typedBody)
  const imageData = `data:image/svg+xml;base64,${btoa(unescape(encodeURIComponent(svg)))}`

  const metadata = {
    name: "Arc Builder Passport",
    description:
      "A builder identity passport generated by Arc Quest Hub using verified onchain activity, XP, reputation, streaks and check-ins.",
    external_url: typedBody.externalUrl,
    image: imageData,
    image_data: svg,
    attributes: [
      { trait_type: "Wallet", value: typedBody.wallet },
      { trait_type: "Level", value: typedBody.level },
      { trait_type: "XP", value: typedBody.xp },
      { trait_type: "Builder Score", value: typedBody.builderScore },
      { trait_type: "Reputation", value: typedBody.reputation },
      { trait_type: "Current Streak", value: typedBody.currentStreak },
      { trait_type: "Best Streak", value: typedBody.bestStreak },
      { trait_type: "Check-ins", value: typedBody.totalCheckIns },
      {
        trait_type: "Badges",
        value: typedBody.badges.length > 0 ? typedBody.badges.join(", ") : "None",
      },
    ],
  }

  try {
    const pinataResponse = await fetch(
      "https://api.pinata.cloud/pinning/pinJSONToIPFS",
      {
        method: "POST",
        headers: {
          Authorization: `Bearer ${pinataJwt}`,
          "Content-Type": "application/json",
        },
        body: JSON.stringify({
          pinataMetadata: {
            name: `arc-builder-passport-${typedBody.wallet}`,
          },
          pinataContent: metadata,
        }),
      },
    )

    if (!pinataResponse.ok) {
      return res.status(502).json({ error: "Failed to upload metadata to IPFS." })
    }

    const result = (await pinataResponse.json()) as { IpfsHash?: string }

    if (!result.IpfsHash) {
      return res.status(502).json({ error: "IPFS upload did not return a CID." })
    }

    return res.status(200).json({
      cid: result.IpfsHash,
      tokenUri: `ipfs://${result.IpfsHash}`,
      gatewayUrl: `https://gateway.pinata.cloud/ipfs/${result.IpfsHash}`,
    })
  } catch {
    return res.status(500).json({ error: "Unexpected metadata upload error." })
  }
}